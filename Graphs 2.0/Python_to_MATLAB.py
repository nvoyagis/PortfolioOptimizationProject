def python_to_matlab_adj_matrix(s: str):
    str_list = s.split('\n')
    matlab = "[\n"
    for i in range(len(str_list)):
        str = str_list[i][2:(len(str_list[i])-1)]
        matlab += str + ';\n'
    str_list[len(str_list) - 1] = str_list[len(str_list) - 1][0:(len(str_list) - 1)]
    print(matlab)

